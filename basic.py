# ------------------------
# 配列の中身を出力
# ------------------------
n = [1, 2, 3]
print(*n)
# 1 2 3

# ------------------------------------------
# 配列の中身をくっつけて(文字列にして)出力
# ------------------------------------------
n = [1, 2, 3]
l = []
for i in range(1, n+1):
    l.append(i)
print("".join(map(str, l)))
# 123


# ------------------------------------------
# 小数点２桁まで表示(format(num, ".2f"))
# ------------------------------------------
n = 23
print(format(n, '.2f'))
# 23.00
