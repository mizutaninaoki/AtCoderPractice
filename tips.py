# それぞれの値をまとめて乗算してからある数で割った余りと、それぞれの数を乗算して割った余りを繰り返した、最終的なあまりは同じ。
# 大きな桁数の計算は非常に時間がかかるため、計算途中も小さい数に抑えた方が早くなる！
# ------------------------------------------------------------------------------------------------------------
