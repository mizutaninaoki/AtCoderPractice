# ------------------
# 三角形の成立条件
# ------------------
# 三角形は2つの辺の長さの和は残りの1つの辺の長さより大きいという特徴があります。 つまり、三角形の3辺の長さを a，b，c とするとき、次の三つの不等式が成り立ちます。
# a < b+c
# b < a+c
# c < a+b


